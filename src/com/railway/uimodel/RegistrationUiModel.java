package com.railway.uimodel;

/**
 *
 * @author Mahesh Nair
 */
public class RegistrationUiModel {
    private String password;
    private String cnfrmPassword;
    private String user;
    private String email;

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the cnfrmPassword
     */
    public String getCnfrmPassword() {
        return cnfrmPassword;
    }

    /**
     * @param cnfrmPassword the cnfrmPassword to set
     */
    public void setCnfrmPassword(String cnfrmPassword) {
        this.cnfrmPassword = cnfrmPassword;
    }

    /**
     * @return the user
     */
    public String getUser() {
        return user;
    }

    /**
     * @param user the user to set
     */
    public void setUser(String user) {
        this.user = user;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
        